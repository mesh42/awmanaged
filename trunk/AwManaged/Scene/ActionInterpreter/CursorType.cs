namespace AwManaged.Scene.ActionInterpreter
{
    /// <summary>
    /// Specifies whether the cursor should be displayed.
    /// </summary>
    public enum CursorType
    {
        /// <summary>
        /// On OR true OR yes: show the mouse. 
        /// </summary>
        On,
        /// <summary>
        /// off OR false OR no: hide the mouse cursor.
        /// </summary>
        Off
    }
}